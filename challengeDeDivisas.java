    private void botonConversionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConversionActionPerformed
        // TODO add your handling code here:
        list1 = listaOrigen.getSelectedItem().toString();
        list2 = listaConvertir.getSelectedItem().toString();
        double fin0 = Double.parseDouble(monto.getText());
        
          if (list1.equals(list2)){
            factor2 = fin0;
        } else if ("USD".equals(list1)) {
            factor1 = USD*fin0;
        } else if ("EUR".equals(list1)) {
            factor1 = EUR*fin0;
        } else if ("LIB".equals(list1)) {
            factor1 = LIB*fin0;
        } else if ("WON".equals(list1)) {
            factor1 = WON*fin0;
        } else if ("YEN".equals(list1)) {
            factor1 = YEN*fin0;
        } else if ("MEX".equals(list1)) {
            factor1 = fin0;
        }
        
          if (list1.equals(list2)){
            factor2 = fin0;
        } else if ("USD".equals(list2)) {
            factor2 = factor1/USD;
        } else if ("EUR".equals(list2)) {
            factor2 = factor1/EUR;
        } else if ("LIB".equals(list2)) {
            factor2 = factor1/LIB;
        } else if ("WON".equals(list2)) {
            factor2 = factor1/WON;
        } else if ("YEN".equals(list2)) {
            factor2 = factor1/YEN;
        } else if ("MEX".equals(list2)) {
            factor2 = factor1;
        }
        
        resultado.setText("Usted tiene " + factor2 + " " + list2);
        
        
        
    }//GEN-LAST:event_botonConversionActionPerformed

